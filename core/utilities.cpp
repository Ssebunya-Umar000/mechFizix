#include"utilities.h"

#include<fstream>

namespace mech {

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////
	struct ProfileFile {
		std::ofstream file;

		ProfileFile()
		{
			this->file.open("profile.mech");
		}

		~ProfileFile()
		{
			this->file.close();
		}

		void write(const char* str)
		{
			this->file << str << '\n';
		}
	};

	ProfileFile profileFile;
	
	ScopeProfiler::~ScopeProfiler()
	{
		profileFile.write((this->name + String(" => ") + toString2(this->timer.elapsedNanoSeconds())).cString());
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////
	struct LogFile {
		Timer timer;
		std::ofstream file;

		LogFile()
		{
			this->file.open("logs.mech");
		}

		~LogFile()
		{
			this->file.close();
		}

		void write(const char* str)
		{
			this->file << "time:" << toString2(timer.currentSeconds()).cString() << "s\n" << str << '\n';
		}
	};

	LogFile logFile;

	void log(const char* str)
	{
		logFile.write(str);
	}
}